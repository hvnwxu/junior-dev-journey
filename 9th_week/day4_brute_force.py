N = int(input())
cnt = 0
for i in range(1, N + 1):
    if i % 3 == 0:
        cnt += 1
        # ⚠️ [핵심 버그] N // 3 이 아니라 i % 3 == 0 이어야 할 것 같아!
        #
        #    지금 코드의 문제:
        #    1) N // 3 → 이건 "N을 3으로 나눈 몫"이야. N이 고정값이니까
        #       루프 안에서 매번 똑같은 결과가 나와.
        #    2) N // 3 == 0 → N이 3 미만일 때만 True (예: N=1, N=2)
        #       N이 14나 36이면 항상 False라서 cnt는 0으로 끝나.
        #
        #    "3의 배수인 숫자를 세는 거"라면:
        #    if i % 3 == 0:  ← i를 3으로 나눈 "나머지"가 0인지 확인
        #
        #    // (몫) vs % (나머지) 헷갈리기 쉬우니까 정리해보면:
        #      9 // 3 = 3  (몫)
        #      9 % 3  = 0  (나머지) ← 배수 판별은 이걸 써야 해!

# ⚠️ 아래 하드코딩은 테스트용으로 넣은 거지?
#    특정 입력에만 맞는 답을 직접 써놓으면 채점에서 다른 입력이 들어올 때 틀려.
#    위의 반복문 로직이 제대로 동작하면 이 부분은 지워도 돼!
if N == 14:
    print(4)

if N == 36:
    print(16)
if cnt != 0:
    print(cnt)