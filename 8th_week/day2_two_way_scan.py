scores = [75, 85, 90, 88, 60]
i = 2 # 기준점 (90점)
count = 0

# 1. 왼쪽 스캔 ⬅️
j = i - 1
while j >= 0:
    if scores[j] < 80:   # 80점 미만이면?
        continue            # 어떻게 해야 할까?
    count += 1
    j -= 1

# 2. 오른쪽 스캔 ➡️
j = i + 1
while j < len(scores):
    if scores[j] < 80: # 80점 미만이면?
        continue         # 어떻게 해야 할까?
    count += 1
    j += 1

print(count)

# ✅ 양방향 스캔 구조 자체는 정확하게 짰어!
# 직접 손으로 추적해보면 이해가 훨씬 빠를 거야:
#
#   왼쪽: scores[1]=85 → 80 이상이니까 count=1, j=0
#         scores[0]=75 → 80 미만이니까 break (여기서 멈춤)
#   오른쪽: scores[3]=88 → 80 이상이니까 count=2, j=4
#           scores[4]=60 → 80 미만이니까 break (여기서 멈춤)
#   최종 결과: count = 2
#
# ❓ 확인해봐야 할 것:
#    이 코드는 "80점 미만이 나오는 순간 멈추는" 방식이야.
#    만약 문제가 "끊기지 않고 연속으로 80점 이상인 사람만" 세는 거라면 맞고,
#    "80점 이상이면 연속이든 아니든 전부" 세는 거라면 break를 빼야 해.
#    문제 조건을 다시 확인해봐!
