# 1. 문제 이해
# - 입력: 정수 2개 (한 줄에 공백으로 구분되어 입력됨)
# - 출력: 두 정수의 합

# 2. 풀이 계획 (한글)
# 1) 한 줄에서 두 개의 숫자를 입력 받는다
# 2) 각각 정수로 변환한다
# 3) 두 수를 더한다
# 4) 결과를 출력한다

# 3. 코드

# 입력 받기
a, b = map(int, input().split())

# 계산
result = a + b

# 출력
print(result)


